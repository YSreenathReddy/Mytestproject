package Variables1;

public class LocalVariables
{

	public static void main(String[] args) 
	{
		final int i=10;
		final int j=20;
		
		for(int k=0;k<i+j;k++)
		{
			
			System.out.println(k);	
		}
		
		System.out.println(i+""+j);
	int x;
	if(args.length>0)
	{
 x=10;
	}
	
	else
	{
	 x=0;
	}
	
	System.out.println(x);
	
}
	//For Local variables none of the modifiers are applicable Other than Final modifier  
}

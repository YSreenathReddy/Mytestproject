package Arrays;

public class Arraydeclaration {

	public static void main(String[] args)
	{
		//One dimensional array
		int[] x=new int[3];
		x[0]=10;
		x[1]=20;
		x[2]=30;
		System.out.println(x[0]+x[1]+x[2]);
		
		//Two dimensional array
		
		int[][] x1=new int[4][];
		x1[0]=new int[2];
		x1[1]=new int[2];
		
		//Multi dimnsion array
		
		int[][][] x2=new int[2][][];
		x2[0]=new int[3][];
		x2[0][0]=new int[1];
		x2[0][1]=new int[2];
		x2[0][2]=new int[3];
		
		
		
		
		
		
		
		

	}

}
